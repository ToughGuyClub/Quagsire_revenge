class Tree:
    def __init__(self, x, y, treetype):
        self.x = x
        self.y = y
        self.treetype = treetype
        self.image = load_image('trees.png')

    def draw(self):
        self.image.clip_draw(0+ 47* self.treetype, 464 , 46, 48, self.x, self.y,100,100)


